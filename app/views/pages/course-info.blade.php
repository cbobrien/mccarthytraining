				<h2>
					Our Repair Shop Assistant and General Service Technician Development Programme
				</h2>
				<p>
					The Bidvest Automotive Training Academy has developed a new and exciting new Repair Shop Assistant (RSA) and General Service Technician Development (GTSD) programme. Every staff member employed as an RSA must successfully complete the full training programme in order to ensure the requisite competence that this role requires in the workshop. This not only promotes and improves efficiency, competency and productivity in our staff, but results in a safer work environment and has the added benefit of improving our CSI (Customer Satisfaction Index) as well as our ESI (Employee Satisfaction Index). All RSAs are developed with the intention to successfully achieve full artisan status in their relevant trades and the exit level assessment for this training program is therefore a National trade test.
				</p>

				<h2>Trade Testing</h2>
				<p>
					The Bidvest Automotive Training Academy is also accredited as a DTTC (Decentralised Trade Test Centre) to level test and trade test our auto-electrical courses, motor mechanic courses and diesel mechanic training courses. These programs each consist of two weeks preparation and two days testing.
				</p>

